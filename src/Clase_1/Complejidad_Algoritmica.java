public class Complejidad_Algoritmica {
/*
    NOTACIÓN "BIG O" o "O grande"
    
    Complejidades Algorítmicas Ordenadas (Mayor a Menor)
    
    O(n!) - Factorial: Crecimiento extremadamente rápido, explora todas las permutaciones. (Ejemplo: fuerza bruta para el viajante de comercio)
    O(2^n) - Exponencial: Crecimiento rápido, operaciones que se duplican con cada elemento. (Ejemplo: fuerza bruta para encontrar subconjuntos que suman un valor objetivo)
    O(n^k) - Polinomial (k > 1): Crecimiento moderado, operaciones que aumentan con una potencia del tamaño de la entrada. (Ejemplo: ordenamiento por burbuja)
    O(n log n) - Logarítmica lineal: Crecimiento moderado, operaciones que aumentan logarítmicamente con el tamaño de la entrada. (Ejemplo: ordenamiento por fusión)
    O(n) - Lineal: Crecimiento proporcional al tamaño de la entrada. (Ejemplo: búsqueda lineal)
    O(log n) - Logarítmica: Crecimiento lento, operaciones que aumentan logarítmicamente con el tamaño de la entrada. (Ejemplo: búsqueda binaria)
    O(1) - Constante: Crecimiento independiente del tamaño de la entrada. (Ejemplo: acceso a un elemento de un arreglo)
*/
}
